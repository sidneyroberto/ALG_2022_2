numero1 = 3
numero2 = 4

# Soma
resultado = numero1 + numero2
print('%d + %d = %d' % (numero1, numero2, resultado))

# Subtração
resultado = numero1 - numero2
print('%d - %d = %d' % (numero1, numero2, resultado))

# Multiplicação
resultado = numero1 * numero2
print('%d * %d = %d' % (numero1, numero2, resultado))

# Divisão
resultado = numero1 / numero2
print('%d / %d = %.2f' % (numero1, numero2, resultado))

# Exponenciação
resultado = numero1 ** numero2
print('%d ** %d = %d' % (numero1, numero2, resultado))

# Módulo
resultado = numero1 % numero2
print('%d %% %d = %d' % (numero1, numero2, resultado))
